void solve() {
  int r,c;
  cin>>r>>c;
  for (int i=0;i<r;i++) {
    if (i%2==0) {
      string s(c,'#');
      cout<<s<<"\n";
    } else {
      if (i%4==3) {
        string s(c-1,'.');
        s='#'+s;
        cout<<s<<"\n";
      } else {
        string s(c-1,'.');
        s+='#';
        cout<<s<<"\n";
      }
    }
  }
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}