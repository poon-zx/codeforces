constexpr int MOD = 1'000'000'007;

constexpr array<array<int, 2>, 4> directions{{
    {0, 1},
    {1, 0},
    {0, -1},
    {-1, 0},
}};

// vector<vector<int>> v(3, vector<int>(4,0) 3x4 filled with 0s

void solve() {
  int n;
  cin>>n;
  int highest=0;
  int h_i=-1;
  int lowest=MOD;
  int l_i=-1;
  for (int i=0;i<n;i++) {
    int c;
    cin>>c;
    if (c>highest) {
      highest=c;
      h_i=i;
    }
    if (c<=lowest) {
      lowest=c;
      l_i=i;
    }
  }
  int res=h_i+(n-1-l_i);
  if (h_i>l_i) {res--;}
  cout<<res;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc{1};
  while (tc--) {
    solve();
  }

  return 0;
}