constexpr ll MOD = 998244353;

constexpr array<array<int, 2>, 4> directions{{
    {0, 1},
    {1, 0},
    {0, -1},
    {-1, 0},
}};

// vector<vector<int>> v(3, vector<int>(4,0) 3x4 filled with 0s

void solve() {
  int n;
  cin>>n;
  vector<ll> dp(4,0);
  dp[0]=1;
  for (int i=0;i<n;i++) {
    ll temp;
    cin>>temp;
    if (temp==2) {
      dp[temp]+=dp[temp];
      dp[temp]%=MOD;
    }
    dp[temp]+=dp[temp-1];
    dp[temp]%=MOD;
  }
  cout<<dp[3]<<"\n";
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc;
  cin>>tc;
  while (tc--) {
    solve();
  }

  return 0;
}