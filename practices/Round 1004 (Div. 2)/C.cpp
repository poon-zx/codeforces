void solve() {
  int n;
  cin>>n;
  for (int i=0;i<10;i++) {
    string s=to_string(n-i);
    int m=0;
    for (auto &ch:s) {
      if (ch<='7') {
        m=max(m,ch-'0'); // closest number to 7
      }
    }
    if (i>=7-m) { // need add at least 7-m numbers
      cout<<i<<"\n";
      return;
    }
  }
}

