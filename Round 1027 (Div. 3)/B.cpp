void solve() {
  int n,k;
  cin>>n>>k;
  string s;
  cin>>s;
  int zero=0,one=0;
  for(char c:s){
    if(c=='0') zero++;
    else one++;
  }
  int x_min=(max(one,zero)-min(one,zero))/2;
  int maxx=one/2+zero/2;
  if(k>=x_min&&k<=maxx&&(k-x_min)%2==0) cout<<"YES";
  else cout<<"NO";
  cout<<"\n";
}

