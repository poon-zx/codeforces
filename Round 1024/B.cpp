void solve() {
  int n;
  cin>>n;
  vector<int> v(n);
  for(int i=0;i<n;i++) cin>>v[i];
  int smaller=0;
  int bigger=0;
  for(int i=1;i<n;i++) {
    if(abs(v[i])<abs(v[0])) smaller++;
    else if (abs(v[i])>abs(v[0])) bigger++;
  }
  if(n%2==1) {
    if(bigger<n/2) cout<<"NO";
    else cout<<"YES";
  } else {
    if(bigger<n/2-1) cout<<"NO";
    else cout<<"YES";
  }
  cout<<"\n";
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc;
  cin>>tc;
  while (tc--) {
    solve();
  }

  return 0;
}