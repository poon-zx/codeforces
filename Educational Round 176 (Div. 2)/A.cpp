void solve() {
  ll n,k;
  cin>>n>>k;
  ll res=1;
  if (k>=n) {
    cout<<1<<"\n";
    return;
  }
  if (res%2==0) n-=(k-1);
  else n-=k;
  res+=n/(k-1);
  if (n%(k-1)!=0) res++;
  cout<<res<<"\n";
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int tc;
  cin>>tc;
  while (tc--) {
    solve();
  }

  return 0;
}