void solve() {
  int n;
  string s;
  cin>>n>>s;
  int cnt_a=0,cnt_b=0;
  for (int i=0;i<n;i++) {
    if(s[i]=='A') cnt_a++;
    else cnt_b++;
  }
  if (s[n-1]==s[n-2]||s[n-1]==s[0]) {
    cout<<(s[n-1]=='A'?"Alice":"Bob")<<"\n";
    return;
  }
  if(cnt_b==1&&s!="BA") {
    cout<<"Alice"<<"\n";
    return;
  }
  cout<<"Bob"<<"\n";
}

