void solve() {
  int n;
  ll x;
  cin>>n>>x;
  ll num=2;
  int power=1;
  while (num<=x) {
    num*=2;
    power+=1;
  }
  num/=2;
  vector<ll> v;
  v.push_back(num);
  for (int i=0;i<n-1;i++) {
    v.push_back(i);
  }
  while (v.size()<n) {
    v.push_back(0);
  }
  for (auto nu:v) {
    cout<<nu<<" ";
  }
  cout<<"\n";
}

